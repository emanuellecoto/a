-- Vista de Usuarios
CREATE OR REPLACE VIEW FIDE_USUARIO_V AS
SELECT ID_USUARIO, NOMBRE, PRIMER_APELLIDO, SEGUNDO_APELLIDO, EMAIL, TELEFONO, ID_ROLE, ID_ESTADO
FROM FIDE_USUARIO_TB;

-- Vista de Doctores
CREATE OR REPLACE VIEW FIDE_DOCTORES_V AS
SELECT ID_DOCTOR, ID_USUARIO, SERVICIO, ID_ESTADO
FROM FIDE_DOCTORES_TB;

-- Vista de Estados
CREATE OR REPLACE VIEW FIDE_ESTADO_V AS
SELECT ID_ESTADO, DESCRIPCION
FROM FIDE_ESTADO_TB;

-- Vista de Roles
CREATE OR REPLACE VIEW FIDE_ROLE_V AS
SELECT ID_ROLE, DESCRIPCION, ID_ESTADO
FROM FIDE_ROLE_TB;

-- Vista de Almacén
CREATE OR REPLACE VIEW FIDE_ALMACEN_V AS
SELECT ID_UBICACION, PISO, HILERA, ID_ESTADO
