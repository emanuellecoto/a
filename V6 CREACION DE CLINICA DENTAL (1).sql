FROM FIDE_SERVICIOS_TB;

-- Vista de Consultorios
CREATE OR REPLACE VIEW FIDE_CONSULTORIO_V AS
SELECT ID_CONSULTORIO, ID_SERVICIO, ID_ESTADO
FROM FIDE_CONSULTORIO_TB;

-- Vista de Inventario
CREATE OR REPLACE VIEW FIDE_INVENTARIO_V AS
